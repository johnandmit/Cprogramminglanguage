#include <stdio.h>
#include <math.h>


int main()
{
    float a, b, P, S;
    a > 0;
    b > 0;
    printf("Enter a, b: ");
    scanf("%f, %f", &a, &b);
    P = a*2+b*2;
    S = a * b;
    printf("if a=%f b=%f then P=%f S=%f\n", a, b, P, S);
    printf("The Rectangle: perimeter=%f, area=%f", P, S);


}